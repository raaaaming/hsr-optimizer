/**
 * 광추 패시브 중 "상시 스탯 보너스"의 해석.
 *
 * 시드(light_cones.passive)에는 Yatta 원본이 그대로 들어있다.
 *
 *   desc   : "장착한 캐릭터의 공격력이 #1[i]% 증가한다. ... 적을 처치하면 #3[i]% ..."
 *   params : { "1": [0.24, 0.28, 0.32, 0.36, 0.4], "3": [...] }
 *
 * params는 자리표시자 번호 => 중첩 1~5단계 배열이다. 어느 자리표시자가 어떤
 * 스탯인지는 한국어 설명문에만 있어서 기계적으로 못 뽑는다. 게다가 상시 효과와
 * 조건부 효과가 한 설명문에 섞여 있어서(#1은 상시, #3은 "적을 처치하면")
 * 전부 더하면 조용히 부풀려진 수치가 나온다.
 *
 * 그래서 여기엔 "어느 자리표시자가 어느 스탯인가"라는 해석만 적는다.
 * 수치 자체는 시드에서 읽으므로 패치로 값이 바뀌면 자동으로 따라온다.
 *
 * 여기 없는 광추는 기본 스탯(HP/공격력/방어력)만 반영되고 패시브는 0이다.
 * 조건부 효과(스택, 턴 제한, 처치 시 등)는 전투 시뮬레이션의 영역이라
 * 여기서 다루지 않는다.
 *
 * 새로 추가할 때:
 *   1. 시드에서 그 광추의 desc와 params를 본다.
 *   2. "장착한 캐릭터의 X가 #n% 증가한다"처럼 조건 없는 항목만 고른다.
 *   3. stat은 gameData.js의 STATS 키를 쓴다.
 */
